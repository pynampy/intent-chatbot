const HIDDEN_SIZE = 5;
const model = tf.sequential();
model.add(
  tf.layers.dense({
    inputShape: [50],
    units: HIDDEN_SIZE,
  })
);
model.add(
  tf.layers.dense({
    units: HIDDEN_SIZE,
  })
);

model.add(
  tf.layers.dense({
    units: 1,
    activation: "softmax",
    units: HIDDEN_SIZE,
  })
);

const ALPHA = 0.1;
model.compile({
  optimizer: tf.train.sgd(ALPHA),
  loss: "meanSquaredError",
});

